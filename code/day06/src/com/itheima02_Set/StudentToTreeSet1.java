package com.itheima02_Set;

public class StudentToTreeSet1 {
    String name;
    int age;

    public StudentToTreeSet1(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "StudentToTreeSet1{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
