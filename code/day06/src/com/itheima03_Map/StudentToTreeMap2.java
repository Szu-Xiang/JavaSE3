package com.itheima03_Map;

public class StudentToTreeMap2 {
    String name;
    int age;

    public StudentToTreeMap2(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "StudentToTreeMap2{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
