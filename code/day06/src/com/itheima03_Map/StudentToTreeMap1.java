package com.itheima03_Map;

public class StudentToTreeMap1 {
    String name;
    int age;

    public StudentToTreeMap1(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "StudentToTreeMap1{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }


}
